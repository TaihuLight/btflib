% This function converts cartesian coordinates specified as an N x 3 array
% into parabolic coordinates according to the equation x_{par} = x_{car} /
% (1 + z_{car]) and returns the parabolic x- and y-coordinates in an N x 2
% array.
function par = cart2par(cart)
    par = cart(:, 1 : 2) ./ repmat(1 + cart(:, 3), 1, 2);
end